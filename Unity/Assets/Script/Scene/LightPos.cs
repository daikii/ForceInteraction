using UnityEngine;
using System.Collections;

public class LightPos : MonoBehaviour 
{
	public PlaneGrid plane;

	void Start() 
	{
		this.transform.position = new Vector3 (plane.lengthScale/2, 30, plane.widthScale/2);
	}
}
